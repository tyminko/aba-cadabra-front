<template>
  <div class="login-wrap">
    <div class="login-container px-sm">
      <div class="mb-sm px-sm">Log In</div>
      <div class="card-content">
        <form
          ref="formRef"
          class="login-box"
          @submit.prevent="handleLogin">
            <px-input
              ref="emailRef"
              v-model="userEmail"
              label="Email"
              type="email"
              :rules="validationRules.required"
              @input="clearMessage" />
            <px-input
              v-model="userPassword"
              label="Password"
              type="password"
              :rules="validationRules.required"
              spellcheck="false"
              autocomplete="current-password"
              @input="clearMessage" />
            <p v-if="message" class="message" :class="{open: message}">
              {{message}}
            </p>
        </form>
        <a href="#" @click.prevent="forgotPassword = true" class="block px-sm text-sm text-gray-900 -mt-sm">
          Forgot my password
        </a>
      </div>
      <div class="flex justify-end">
        <button :disabled="!enableLogin" class="w-auto my-base px-sm" @click="handleLogin">
          <span>Log In</span>
        </button>
      </div>
    </div>
    <transition name="push">
      <forgot-form
        v-if="forgotPassword"
        :class="{open: forgotPassword}"
        class="forgot-form"
        @close="forgotPassword = false" />
    </transition>
  </div>
</template>

<script setup>
import { ref, computed, onMounted } from 'vue'
import { auth } from '../../../lib/firebase'
import ForgotForm from './ForgotPasswordForm'
import PxInput from '../UI/inputs/PxInput'

// Ref
const formRef = ref(null)
const emailRef = ref(null)
const userEmail = ref('')
const userPassword = ref('')
const message = ref('')
const formValid = ref(true)
const forgotPassword = ref(false)

// Validation Rule
const validationRules = {
  required: value => !!value || 'Required',
  email: value => /.+@.+\..+/.test(value) || 'E-mail must be valid'
}

// Computed
const enableLogin = computed(() => formValid.value)

// Method
const handleLogin = async () => {
  try {
    await auth.signInWithEmailAndPassword(userEmail.value, userPassword.value)
  } catch (err) {
    console.log(err)
    if (err.code === 'auth/wrong-password' || err.code === 'auth/user-not-found') {
      message.value = 'Wrong email or password.'
    } else {
      message.value = err.message
    }
  }
}

const clearMessage = () => {
  if (message.value) message.value = ''
}

// Lifecycle
onMounted(() => {
  emailRef.value?.focu ()
})
</script>

